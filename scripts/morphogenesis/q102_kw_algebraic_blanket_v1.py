def compute_commutator_norm(A_op, B_op):
    """‖A·B − B·A‖_F"""
    return float(np.linalg.norm(A_op @ B_op - B_op @ A_op, 'fro'))


def compute_double_commutator(D_op, a_op, b_opp):
    """[[D, a], b°] = (D·a − a·D)·b° − b°·(D·a − a·D)"""
    inner = D_op @ a_op - a_op @ D_op
    return inner @ b_opp - b_opp @ inner


